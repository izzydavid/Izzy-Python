# Logical Operators
print(4 > 5)
print(4 < 5)
print(4 == 5)
print('hello' == 'hello')
print('a' > 'b')
print('a' > 'A')
print(1 < 2 < 3 < 4)
print(1 < 2 > 3 < 4)  # Short Circuiting when first false is introduced.
print(1 >= 0)
print(1 >= 0)
print(0 == 0)  # Equal
print(0 != 1)  # Not equal
# and or not
print(not (True))
